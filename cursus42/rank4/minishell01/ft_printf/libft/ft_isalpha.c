#include "libft.h"

int		ft_isalpha(int asc)
{
	return ((asc >= 'A' && asc <= 'Z') || (asc >= 'a' && asc <= 'z') ||
		(asc >= -82 && asc <= -57) || (asc <= -42 && asc >= -17));
}
